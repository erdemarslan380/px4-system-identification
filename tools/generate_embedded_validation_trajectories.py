#!/usr/bin/env python3

from __future__ import annotations

import argparse
import struct
from pathlib import Path


TRAJ_IDS = [100, 101, 102, 103, 104]
FLOATS_PER_SAMPLE = 11
SAMPLE_STRUCT = struct.Struct("<" + "f" * FLOATS_PER_SAMPLE)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--assets-dir", required=True)
    parser.add_argument("--output-cpp", required=True)
    return parser.parse_args()


def read_samples(path: Path) -> list[tuple[float, ...]]:
    data = path.read_bytes()

    if len(data) % SAMPLE_STRUCT.size != 0:
        raise RuntimeError(f"{path} has invalid size {len(data)}")

    return [
        SAMPLE_STRUCT.unpack_from(data, offset)
        for offset in range(0, len(data), SAMPLE_STRUCT.size)
    ]


def format_sample(sample: tuple[float, ...]) -> str:
    def format_value(value: float) -> str:
        text = f"{value:.9g}"
        if "." not in text and "e" not in text and "E" not in text:
            text += ".0"
        return text + "f"

    values = ", ".join(format_value(value) for value in sample)
    return f"    {{{values}}},\n"


def main() -> None:
    args = parse_args()
    assets_dir = Path(args.assets_dir)
    output_cpp = Path(args.output_cpp)

    trajectory_samples: dict[int, list[tuple[float, ...]]] = {}

    for traj_id in TRAJ_IDS:
        path = assets_dir / f"id_{traj_id}.traj"
        trajectory_samples[traj_id] = read_samples(path)

    lines: list[str] = []
    lines.append("// Auto-generated by generate_embedded_validation_trajectories.py\n")
    lines.append("#include \"trajectory_reader.hpp\"\n")
    lines.append("#include \"embedded_validation_trajectories.hpp\"\n\n")
    lines.append("namespace {\n")

    for traj_id in TRAJ_IDS:
        samples = trajectory_samples[traj_id]
        lines.append(f"static const TrajSample kTrajectory{traj_id}[] = {{\n")
        for sample in samples:
            lines.append(format_sample(sample))
        lines.append("};\n\n")

    lines.append("} // namespace\n\n")
    lines.append(
        "bool getEmbeddedValidationTrajectory(uint8_t traj_id, const TrajSample *&samples, uint32_t &count)\n{\n"
    )
    lines.append("    switch (traj_id) {\n")

    for traj_id in TRAJ_IDS:
        samples = trajectory_samples[traj_id]
        lines.append(f"    case {traj_id}:\n")
        lines.append(f"        samples = kTrajectory{traj_id};\n")
        lines.append(f"        count = {len(samples)}u;\n")
        lines.append("        return true;\n")

    lines.append("    default:\n")
    lines.append("        samples = nullptr;\n")
    lines.append("        count = 0;\n")
    lines.append("        return false;\n")
    lines.append("    }\n")
    lines.append("}\n")

    output_cpp.write_text("".join(lines))


if __name__ == "__main__":
    main()
