def archive_gazebo_truth_log(
    results_dir: Path,
    eval_index: int,
    payload: dict,
    raw_log_dir: Path,
    *,
    result_json: Optional[Path] = None,
) -> None:
    if not raw_log_dir.exists():
        return

    candidates = [
        path for path in raw_log_dir.glob("*.csv")
        if path.is_file()
    ]
    if not candidates:
        return

    src = max(candidates, key=lambda path: path.stat().st_mtime)
    dst = results_dir / "gazebo_truth_traces" / f"eval_{eval_index:05d}.csv"
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)
    payload["gazebo_truth_log_archive"] = str(dst)

    if result_json is not None and result_json.exists():
        try:
            on_disk = json.loads(result_json.read_text(encoding="utf-8"))
        except Exception:
            on_disk = {}
        on_disk["gazebo_truth_log_archive"] = str(dst)
        atomic_write_json(result_json, on_disk)


def copy_tree_contents(src: Path, dst: Path, ignore: Optional[callable] = None) -> None:
    if not src.exists():
        return
    for item in src.iterdir():
        if ignore is not None and item.name in ignore(src, [item.name]):
            continue
        target = dst / item.name
        if item.is_dir():
            shutil.copytree(item, target, dirs_exist_ok=True, ignore=ignore)
        elif item.is_symlink():
            if target.exists() or target.is_symlink():
                target.unlink()
            target.symlink_to(item.resolve())
        else:
            shutil.copy2(item, target)


class Px4CtlLite:
    def __init__(self, rootfs: Path, bin_dir: Path, instance_id: int):
        self.rootfs = rootfs
        self.bin_dir = bin_dir
        self.instance_id = instance_id

    def command(self, command_line: str, timeout_s: float = 6.0) -> str:
        parts = command_line.split()
        if not parts:
            return ""
        exe = self.bin_dir / f"px4-{parts[0]}"
        proc = subprocess.run(
            [str(exe), "--instance", str(self.instance_id), *parts[1:]],
            cwd=str(self.rootfs),
            capture_output=True,
            text=True,
            timeout=timeout_s,
        )
        out = (proc.stdout or "") + (proc.stderr or "")
        if proc.returncode != 0:
            raise RuntimeError(out.strip() or f"{exe.name} failed")
        return out


