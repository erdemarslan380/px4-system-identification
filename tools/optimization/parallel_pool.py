def atomic_write_json(path: Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_name(f".{path.name}.tmp")
    tmp.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    tmp.replace(path)


def write_param_file(path: Path, params: Dict[str, float], profile: ControllerProfile) -> None:
    write_controller_param_file(
        path,
        profile,
        params,
        header="Autogenerated by parallel_pool.py",
    )


def archive_eval_trace(results_dir: Path, eval_index: int, payload: dict, result_json: Optional[Path] = None) -> None:
    raw_log = str(payload.get("tracking_log", "")).strip()
    if not raw_log:
        return

    src = Path(raw_log)
    if not src.exists():
        return

    dst = results_dir / "eval_traces" / f"eval_{eval_index:05d}.csv"
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)
    payload["tracking_log_archive"] = str(dst)

    if result_json is not None and result_json.exists():
        try:
            on_disk = json.loads(result_json.read_text(encoding="utf-8"))
        except Exception:
            on_disk = {}
        on_disk["tracking_log_archive"] = str(dst)
        atomic_write_json(result_json, on_disk)


